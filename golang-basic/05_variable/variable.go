package main

import "fmt"

func main() {

	var name string
	var angka int
	var boolean bool

	name = "sandi"
	fmt.Println(name)

	name = "salsa"
	fmt.Println(name)

	angka = 1
	fmt.Println(angka)

	boolean = false
	fmt.Println(boolean)

	var findname = "sansal"
	fmt.Println(findname)

	var umur = 18
	fmt.Println(umur)

	kelas := 11
	fmt.Println(kelas)

	var (
		namasaya = "nama saya = sandi"
		namakamu = "nama kamu = salsa"
	)

	fmt.Println(namasaya)
	fmt.Println(namakamu)
}
